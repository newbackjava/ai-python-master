# 연산자 --> 간단한 처리를 하는 기호
# 산술연산자(+, - , *, /(실수포함), //(정수만)), %(나머지)

n1 = 777
n2 = 100

print(n1, "과 ", n2, "의 합은 ", n1 + n2);
print(n1, "과 ", n2, "의 차는 ", n1 - n2);
print(n1, "과 ", n2, "의 곱은 ", n1 * n2);
print(n1, "과 ", n2, "의 나눔은 ", n1 / n2);
print(n1, "과 ", n2, "의 나눔은 ", n1 // n2);
print(n1, "과 ", n2, "의 나머지는 ", n1 % n2);